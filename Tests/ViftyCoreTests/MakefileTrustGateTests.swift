        XCTAssertTrue(makefile.contains("VALIDATION_EVIDENCE_SOURCE_SHA ?="))
        XCTAssertTrue(makefile.contains("VALIDATION_EVIDENCE_SOURCE_ARTIFACT ?="))
        XCTAssertTrue(makefile.contains("VALIDATION_EVIDENCE_RELEASE_SUMMARY ?="))
