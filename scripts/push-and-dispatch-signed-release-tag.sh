    end
    exit 2 unless run.is_a?(Hash)
    run_id, workflow_id, tag, commit, repository, journaled_at, evidence_path = ARGV
    signed_actor = JSON.parse(File.read(evidence_path)).fetch("authenticatedActor")
    abort("release run ID mismatch") unless run["id"] == run_id.to_i
    abort("release workflow ID mismatch") unless run["workflow_id"] == workflow_id.to_i
    abort("release workflow name mismatch") unless run["name"] == "Release"
    abort("release workflow path mismatch") unless run["path"] == ".github/workflows/release.yml"
    abort("release run title mismatch") unless run["display_title"] == "Release #{tag}"
    abort("release run event mismatch") unless run["event"] == "push"
    abort("release run head ref mismatch") unless run["head_branch"] == tag
    abort("release run head SHA mismatch") unless run["head_sha"] == commit
    abort("release run must be its first attempt") unless run["run_attempt"] == 1
    abort("release run actor mismatch") unless
      run.dig("actor", "login") == signed_actor["login"] &&
        run.dig("actor", "id") == signed_actor["id"]
    abort("release run repository mismatch") unless run.dig("repository", "full_name") == repository
