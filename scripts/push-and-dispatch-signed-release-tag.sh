read_exact_ref_response() {
  local namespace="$1"
  local name="$2"
  local response api_status parsed encoded_body
  EXACT_REF_HTTP_STATUS=""
  EXACT_REF_BODY=""
  set +e
  response="$(safe_gh api --hostname github.com --include \
    -H 'Cache-Control: no-cache' -H 'Pragma: no-cache' \
    "repos/${REPOSITORY}/git/ref/${namespace}/${name}" \
    2>"${scratch}/exact-${namespace}-ref.stderr")"
  api_status=$?
  set -e
  if ! parsed="$("${RUBY_BIN}" -rbase64 -e '
    response = STDIN.read
    statuses = []
    response.to_enum(:scan, /^HTTP\/\S+\s+(\d{3})[^\r\n]*\r?$/).each do
      match = Regexp.last_match
      statuses << [match[1], match.end(0)]
    end
    abort("exact-ref response must contain one HTTP status") unless statuses.length == 1
    status, status_end = statuses.fetch(0)
    separator = response.match(/\r?\n\r?\n/, status_end)
    body = separator ? response.byteslice(separator.end(0)..) : ""
    abort("successful exact-ref response lacks a body") if status == "200" && body.empty?
    print status, "\t", Base64.strict_encode64(body)
  ' <<< "${response}")"; then
    return 1
  fi
  EXACT_REF_HTTP_STATUS="${parsed%%$'\t'*}"
  encoded_body="${parsed#*$'\t'}"
  EXACT_REF_BODY="$("${RUBY_BIN}" -rbase64 -e \
    'print Base64.strict_decode64(ARGV.fetch(0))' "${encoded_body}")"
  if [[ "${api_status}" -eq 0 && "${EXACT_REF_HTTP_STATUS}" == "200" ]]; then
    return 0
  fi
  if [[ "${api_status}" -ne 0 && "${EXACT_REF_HTTP_STATUS}" == "404" ]]; then
    return 0
  fi
  return 1
}

