postpush_time="$(/bin/date -u +%Y-%m-%dT%H:%M:%SZ)"
"${RUBY_BIN}" "${committed_root}/${VALIDATOR_PATH}" \
  --root "${ROOT_DIR}" \
  --evidence "${postpush_evidence}" \
  --repository "${REPOSITORY}" \
  --tag "${TAG}" \
  --commit "${COMMIT}" \
  --tagger-time "${TAGGER_TIME}" \
  --current-time "${postpush_time}" \
  --expected-existing-tag-object "${TAG_OBJECT}" \
  > "${scratch}/postpush-governance-validation.json"
if ! "${RUBY_BIN}" -rjson -rtime -e '
  signed = JSON.parse(File.read(ARGV.fetch(0)))
  post = JSON.parse(File.read(ARGV.fetch(1)))
  repository, tag, commit, object = ARGV.drop(2)
  abort("post-push governance is not a live authorized readback") unless
    post["status"] == "passed" &&
      post["releaseAuthorized"] == true &&
      post["evidenceScope"] == "administrator-posttag" &&
      post["apiHost"] == "github.com" &&
      post["dataSource"] == "github-api-live" &&
      post["liveAuthenticatedGitHubReadback"] == true
  abort("post-push governance identity mismatch") unless
    post["repository"] == repository &&
      post["authenticatedActor"] == signed["authenticatedActor"] &&
      post["releaseTag"] == tag &&
      post["expectedMainSHA"] == commit &&
      post["tagAbsentVerified"] == false &&
      post["existingTagVerified"] == true &&
      post["existingTagObjectSHA"] == object
  started = Time.iso8601(post.fetch("observationStartedAt"))
  observed = Time.iso8601(post.fetch("observedAt"))
  now = Time.now.utc
  abort("post-push governance timestamps are invalid") unless
    observed >= started && now >= observed && now.to_i - started.to_i <= 900
  %w[releaseEnvironmentEvidence tagRulesetEvidence releaseSecrets].each do |key|
    abort("post-push governance #{key} differs from signed evidence") unless post[key] == signed[key]
  end
' "${signed_evidence}" "${postpush_evidence}" \
  "${REPOSITORY}" "${TAG}" "${COMMIT}" "${TAG_OBJECT}"; then
  echo "error: post-push administrator governance does not preserve the signed release contract" >&2
  exit 65
fi

TRIGGER_STATE="waiting-for-tag-push-run"
