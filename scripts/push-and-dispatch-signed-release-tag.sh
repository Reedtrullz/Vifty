classify_remote_tag() {
  local classification tag_json tag_status
  if ! read_exact_ref_response tags "${TAG}"; then
    REMOTE_TAG_STATE="unknown"
    return 0
  fi
  if [[ "${EXACT_REF_HTTP_STATUS}" == "404" ]]; then
    REMOTE_TAG_STATE="absent"
    return 0
  fi
  set +e
  classification="$("${RUBY_BIN}" -rjson -e '
    ref = JSON.parse(STDIN.read)
    name, object = ARGV
    abort("exact tag ref response must be an object") unless ref.is_a?(Hash)
    abort("exact tag ref name mismatch") unless ref["ref"] == "refs/tags/#{name}"
    if ref.dig("object", "type") == "tag" &&
       ref.dig("object", "sha") == object
      print "candidate"
    else
      print "mismatched"
    end
  ' "${TAG}" "${TAG_OBJECT}" <<< "${EXACT_REF_BODY}")"
  tag_status=$?
  set -e
  if [[ "${tag_status}" -ne 0 ]]; then
    REMOTE_TAG_STATE="unknown"
    return 0
  fi
  if [[ "${classification}" != "candidate" ]]; then
    REMOTE_TAG_STATE="${classification}"
    return 0
  fi

  set +e
  tag_json="$(safe_gh api --hostname github.com \
    -H 'Cache-Control: no-cache' -H 'Pragma: no-cache' \
    "repos/${REPOSITORY}/git/tags/${TAG_OBJECT}" 2>/dev/null)"
  tag_status=$?
  set -e
  if [[ "${tag_status}" -ne 0 ]]; then
    REMOTE_TAG_STATE="unknown"
    return 0
  fi
  if "${RUBY_BIN}" -rjson -e '
    tag = JSON.parse(STDIN.read)
    expected_name, expected_commit, expected_object = ARGV
    abort("remote annotated tag object SHA mismatch") unless tag["sha"] == expected_object
    abort("remote annotated tag name mismatch") unless tag["tag"] == expected_name
    abort("remote annotated tag target mismatch") unless
      tag.dig("object", "type") == "commit" &&
        tag.dig("object", "sha") == expected_commit
  ' "${TAG}" "${COMMIT}" "${TAG_OBJECT}" <<< "${tag_json}" >/dev/null 2>&1; then
    REMOTE_TAG_STATE="exact"
  else
    REMOTE_TAG_STATE="mismatched"
  fi
}

for ((readback_attempt = 1; readback_attempt <= 30; readback_attempt++)); do
  classify_remote_tag
  if [[ "${REMOTE_TAG_STATE}" == "exact" || "${REMOTE_TAG_STATE}" == "mismatched" ]]; then
    break
  fi
  [[ "${readback_attempt}" == "30" ]] || /bin/sleep 2
done

case "${REMOTE_TAG_STATE}" in
  exact)
    TAG_PUSHED=1
    CURRENT_STAGE="tag-pushed-readback-pending"
    if [[ "${PUSH_EXIT_CODE}" -eq 0 ]]; then
      if [[ "${PUSH_OWNERSHIP}" == "created-by-this-transaction" ]]; then
        PUSH_DISPOSITION="accepted-exact"
        write_receipt "tag-pushed-run-observation-pending" "${CURRENT_STAGE}"
      else
        PUSH_DISPOSITION="exact-tag-not-created-by-this-transaction"
        write_receipt "push-ownership-not-established" "${CURRENT_STAGE}"
        FAILURE_RECEIPT_CLASSIFIED=1
        echo "error: push did not prove exact new-tag ownership; ${TAG} is retired and its tag-push workflow state must be inspected" >&2
        exit 75
      fi
    else
      if [[ "${push_output}" == *"[rejected]"* ||
            "${push_output}" == *"stale info"* ||
            "${push_output}" == *"remote rejected"* ]]; then
        PUSH_DISPOSITION="definite-rejection-remote-exact"
        write_receipt "push-definite-rejection-remote-exact" "${CURRENT_STAGE}"
      else
        PUSH_DISPOSITION="ambiguous-response-remote-exact"
        write_receipt "push-response-ambiguous-remote-exact" "${CURRENT_STAGE}"
      fi
      FAILURE_RECEIPT_CLASSIFIED=1
      echo "error: nonzero tag-push response retires ${TAG}; its workflow may already exist and this tag must not be reused" >&2
      exit 75
    fi
    ;;
  absent)
    PUSH_DISPOSITION="failed-remote-absent"
    write_receipt "push-failed-tag-absent" "${CURRENT_STAGE}"
    echo "error: exact tag push did not create the remote tag (push exit ${PUSH_EXIT_CODE})" >&2
    exit 69
    ;;
  mismatched)
    PUSH_DISPOSITION="remote-mismatched"
    write_receipt "push-result-remote-mismatched" "${CURRENT_STAGE}"
    echo "error: github.com tag readback resolves to a different object; no deletion was attempted" >&2
    exit 65
    ;;
  *)
    REMOTE_TAG_STATE="unknown"
    PUSH_DISPOSITION="remote-unknown"
    write_receipt "push-result-remote-unknown" "${CURRENT_STAGE}"
    echo "error: exact remote tag state is unknown after push exit ${PUSH_EXIT_CODE}; no deletion was attempted" >&2
    exit 69
    ;;
esac

postpush_workflow_id="$(active_release_workflow_id)"
if [[ "${postpush_workflow_id}" != "${WORKFLOW_ID}" ]]; then
  echo "error: active Release workflow identity changed after tag creation" >&2
  exit 65
fi

if [[ "$(remote_main_sha)" != "${COMMIT}" ]]; then
