for ((attempt = 1; attempt <= 30; attempt++)); do
  set +e
  runs_json="$(safe_gh run list \
    --repo "github.com/${REPOSITORY}" \
    --workflow "${WORKFLOW_ID}" \
    --event push \
    --all \
    --limit 100 \
    --json databaseId,displayTitle,event,headBranch,headSha,status,url,workflowDatabaseId,workflowName,createdAt,attempt \
    2>"${scratch}/run-list.stderr")"
  run_list_status=$?
  set -e
  if [[ "${run_list_status}" -ne 0 ]]; then
    /bin/sleep 2
    continue
  fi
  set +e
  "${RUBY_BIN}" -rjson -rtime -e '
    begin
      runs = JSON.parse(STDIN.read)
    rescue JSON::ParserError
      exit 3
    end
    exit 3 unless runs.is_a?(Array) && runs.all? { |run| run.is_a?(Hash) }
    tag, commit, workflow_id, journaled_at, output_path = ARGV
    title = "Release #{tag}"
    matches = runs.select { |run| run["displayTitle"] == title }
    exit 2 if matches.empty?
    abort("tag push matched more than one release run") unless matches.length == 1
    run = matches.fetch(0)
    id = run["databaseId"]
    abort("release run ID is invalid") unless id.is_a?(Integer) && id.positive?
    abort("release run listing workflow mismatch") unless
      run["workflowDatabaseId"] == workflow_id.to_i && run["workflowName"] == "Release"
    abort("release run listing event/ref mismatch") unless
      run["event"] == "push" && run["headBranch"] == tag && run["headSha"] == commit
    abort("release run listing must be its first attempt") unless run["attempt"] == 1
    begin
      created_at = Time.iso8601(run.fetch("createdAt")).utc
      journaled = Time.iso8601(journaled_at).utc
    rescue KeyError, ArgumentError
      abort("release run listing creation time is invalid")
    end
    now = Time.now.utc
    abort("release run predates the tag-push journal") if created_at < journaled - 60
    abort("release run creation time is implausibly in the future") if created_at > now + 60
    File.write(output_path, JSON.generate(run))
  ' "${TAG}" "${COMMIT}" "${WORKFLOW_ID}" "${TAG_PUSH_JOURNALED_AT}" \
    "${matching_run}" <<< "${runs_json}"
  match_status=$?
  set -e
  if [[ "${match_status}" -eq 0 ]]; then
    RUN_ID="$("${RUBY_BIN}" -rjson -e '
      value = JSON.parse(File.read(ARGV.fetch(0))).fetch("databaseId")
      abort("release run ID is invalid") unless value.is_a?(Integer) && value.positive?
      print value
    ' "${matching_run}")"
    run_found=1
    break
  fi
  if [[ "${match_status}" -eq 1 ]]; then
    TRIGGER_STATE="ambiguous"
    TRIGGER_CORRELATION="ambiguous"
    CURRENT_STAGE="tag-push-run-ambiguous"
    write_receipt "tag-push-run-ambiguous" "${CURRENT_STAGE}"
    FAILURE_RECEIPT_CLASSIFIED=1
    echo "error: tag push matched multiple or conflicting Release runs; do not rerun, dispatch manually, or reuse this tag" >&2
    exit 65
  fi
  /bin/sleep 2
done

