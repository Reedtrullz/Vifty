  prior_receipt_result="$("${RUBY_BIN}" -rjson -e '
    receipt = JSON.parse(File.read(ARGV.fetch(0)))
    abort("receipt must be an object") unless receipt.is_a?(Hash)
    repository, tag, commit = ARGV.drop(1)
    abort("receipt schema mismatch") unless receipt["schemaVersion"] == 2
    abort("receipt repository mismatch") unless receipt["repository"] == repository
    abort("receipt tag mismatch") unless receipt["releaseTag"] == tag
    abort("receipt commit mismatch") unless receipt["commitSHA"] == commit
    abort("receipt retry flag must remain false") unless receipt["receiptAuthorizesRetry"] == false
    retryable =
      receipt["status"] == "validated-pre-push" &&
      receipt["stage"] == "validated-pre-push" &&
      receipt["tagPushed"].nil? &&
      receipt["tagPushConfirmed"] == false &&
      receipt["workflowTrigger"] == "push" &&
      receipt["workflowRunObserved"] == false &&
      receipt["workflowRunVerified"] == false &&
      receipt["pushExitCode"].nil? &&
      receipt["remoteTagState"] == "not-checked" &&
      receipt["triggerState"] == "not-observed" &&
      receipt["tagPushJournaledAt"].nil? &&
      receipt["pushDisposition"] == "not-attempted" &&
      receipt["pushOwnership"] == "not-established"
    if retryable
      print "safe-pre-mutation"
      exit 0
    end
    print "retired-after-mutation-boundary"
    exit 2
  ' "${RECEIPT_PATH}" "${REPOSITORY}" "${TAG}" "${COMMIT}" 2>&1)"
  prior_receipt_status=$?
  set -e
  if [[ "${prior_receipt_status}" -ne 0 ]]; then
    echo "error: existing release receipt blocks this transaction (${prior_receipt_result}); inspect it without overwriting or reusing the tag" >&2
    exit 75
  fi
fi

for path in \
  "${SELF_PATH}" \
  "${GOVERNANCE_TOOL_PATH}" \
  "scripts/check-release-environment.sh" \
