    end
    now = Time.now.utc
    abort("release run predates the tag-push journal") if created_at < journaled - 60
    abort("release run createdAt is implausibly in the future") if created_at > now + 60
    print run["html_url"]
  ' "${RUN_ID}" "${WORKFLOW_ID}" "${TAG}" "${COMMIT}" \
