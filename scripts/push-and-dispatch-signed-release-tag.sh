write_receipt() {
  local status="$1"
  local stage="$2"
  local receipt_dir receipt_tmp now
  [[ "${RECEIPT_READY}" == "1" ]] || return 0
  receipt_dir="$(dirname "${RECEIPT_PATH}")"
  if [[ -L "${TRANSACTION_ROOT}" || ! -d "${TRANSACTION_ROOT}" ||
        -L "${receipt_dir}" || ! -d "${receipt_dir}" ||
        -L "${RECEIPT_PATH}" ||
        ( -e "${RECEIPT_PATH}" && ! -f "${RECEIPT_PATH}" ) ]]; then
    echo "error: fixed receipt path changed to an unsafe filesystem object" >&2
    return 1
  fi
  now="$(/bin/date -u +%Y-%m-%dT%H:%M:%SZ)"
  receipt_tmp="$(mktemp "${receipt_dir}/.${TAG}.receipt.XXXXXX")"
  /bin/chmod 600 "${receipt_tmp}"
  "${RUBY_BIN}" -rjson -e '
    status, stage, repository, tag, commit, tag_object, evidence_sha,
      tag_push_journaled_at, tag_pushed, workflow_run_observed,
      workflow_run_verified, push_exit_code, remote_tag_state, trigger_state,
      trigger_correlation, push_disposition, push_ownership,
      workflow_id, run_id, run_url, observed_at = ARGV
    tag_pushed_value =
      if remote_tag_state == "exact"
        true
      elsif remote_tag_state == "absent"
        false
      end
    value = {
      "schemaVersion" => 2,
      "status" => status,
      "stage" => stage,
      "repository" => repository,
      "releaseTag" => tag,
      "commitSHA" => commit,
      "tagObjectSHA" => tag_object.empty? ? nil : tag_object,
      "signedGovernanceEvidenceSHA256" => evidence_sha.empty? ? nil : evidence_sha,
      "tagPushJournaledAt" => tag_push_journaled_at.empty? ? nil : tag_push_journaled_at,
      "tagPushed" => tag_pushed_value,
      "tagPushConfirmed" => tag_pushed == "1",
      "workflowTrigger" => "push",
      "workflowRunObserved" => workflow_run_observed == "1",
      "workflowRunVerified" => workflow_run_verified == "1",
      "pushExitCode" => push_exit_code.empty? ? nil : push_exit_code.to_i,
      "remoteTagState" => remote_tag_state,
      "triggerState" => trigger_state,
      "triggerCorrelation" => trigger_correlation,
      "pushDisposition" => push_disposition,
      "pushOwnership" => push_ownership,
      "receiptAuthorizesRetry" => false,
      "workflowID" => workflow_id.empty? ? nil : workflow_id.to_i,
      "workflowRunID" => run_id.empty? ? nil : run_id.to_i,
      "workflowRunURL" => run_url.empty? ? nil : run_url,
      "observedAt" => observed_at
    }
    puts JSON.pretty_generate(value)
  ' "${status}" "${stage}" "${REPOSITORY}" "${TAG}" "${COMMIT}" "${TAG_OBJECT}" \
    "${SIGNED_EVIDENCE_SHA}" "${TAG_PUSH_JOURNALED_AT}" "${TAG_PUSHED}" \
    "${WORKFLOW_RUN_OBSERVED}" "${WORKFLOW_RUN_VERIFIED}" \
    "${PUSH_EXIT_CODE}" "${REMOTE_TAG_STATE}" "${TRIGGER_STATE}" \
    "${TRIGGER_CORRELATION}" "${PUSH_DISPOSITION}" \
    "${PUSH_OWNERSHIP}" "${WORKFLOW_ID}" "${RUN_ID}" "${RUN_URL}" \
    "${now}" \
    > "${receipt_tmp}"
  /bin/chmod 600 "${receipt_tmp}"
  "${RUBY_BIN}" -e 'File.open(ARGV.fetch(0), "r") { |file| file.fsync }' "${receipt_tmp}"
  if [[ -L "${receipt_dir}" || ! -d "${receipt_dir}" ||
        -L "${RECEIPT_PATH}" ||
        ( -e "${RECEIPT_PATH}" && ! -f "${RECEIPT_PATH}" ) ]]; then
    /bin/unlink "${receipt_tmp}" 2>/dev/null || true
    echo "error: fixed receipt path changed before atomic replacement" >&2
    return 1
  fi
  /bin/mv -f -- "${receipt_tmp}" "${RECEIPT_PATH}"
  /bin/chmod 600 "${RECEIPT_PATH}"
  /bin/sync
}

create_retirement_marker() {
  local created_at
  created_at="${TAG_PUSH_JOURNALED_AT}"
  if [[ -L "${RETIREMENT_MARKER_PATH}" || -e "${RETIREMENT_MARKER_PATH}" ]]; then
    echo "error: durable retired-tag marker already exists for ${TAG}" >&2
    return 1
  fi
  "${RUBY_BIN}" -rjson -e '
    path, repository, tag, commit, tag_object, created_at = ARGV
    value = {
      "schemaVersion" => 1,
      "status" => "retired-at-push-boundary",
      "repository" => repository,
      "releaseTag" => tag,
      "commitSHA" => commit,
      "tagObjectSHA" => tag_object,
      "createdAt" => created_at,
      "retiredTag" => true,
      "authorizesRetry" => false
    }
    flags = File::WRONLY | File::CREAT | File::EXCL
    File.open(path, flags, 0o600) do |file|
      file.write(JSON.pretty_generate(value) + "\n")
      file.flush
      file.fsync
    end
    File.open(File.dirname(path), File::RDONLY) { |directory| directory.fsync }
  ' "${RETIREMENT_MARKER_PATH}" "${REPOSITORY}" "${TAG}" "${COMMIT}" \
    "${TAG_OBJECT}" "${created_at}"
  /bin/chmod 600 "${RETIREMENT_MARKER_PATH}"
}

scratch="$(mktemp -d "${TMPDIR:-/tmp}/vifty-release-push.XXXXXX")"
cleanup() {
  local exit_status=$?
  trap - EXIT
  if [[ "${exit_status}" -ne 0 && "${TAG_PUSHED}" == "1" &&
        "${FAILURE_RECEIPT_CLASSIFIED}" != "1" ]]; then
    set +e
    write_receipt "failed-after-tag-push" "${CURRENT_STAGE}"
    set -e
  fi
  if [[ "${LOCK_HELD}" == "1" && -f "${LOCK_PATH}" && ! -L "${LOCK_PATH}" &&
        "$(< "${LOCK_PATH}")" == "$$" ]]; then
    /bin/unlink "${LOCK_PATH}" 2>/dev/null || true
  fi
  /bin/rm -rf "${scratch}"
  exit "${exit_status}"
}
