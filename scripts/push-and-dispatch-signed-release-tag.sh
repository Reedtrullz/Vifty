usage() {
  cat >&2 <<'USAGE'
Usage: scripts/push-and-dispatch-signed-release-tag.sh --tag v<version> --commit <sha> [options]

Revalidates one existing local SSH-signed annotated release tag, creates that
exact absent tag on github.com with a compare-and-swap push, reads it back, and
observes the unique first-attempt Release run generated by GitHub's tag-push
trigger. The local receipt is evidence only and never authorizes retry or tag
reuse. Any failure after the tag push begins permanently retires that tag.
Observe only that original tag-push run/release; never manually dispatch or
rerun it. Cut a new patch only if the original transaction did not publish.

Options:
  --repository <owner/repo>  GitHub repository (default: Reedtrullz/Vifty).
  --tag <tag>                Existing tag, formatted v<major>.<minor>.<patch>.
  --commit <sha>             Exact 40-character protected-main/tag commit.
USAGE
}

